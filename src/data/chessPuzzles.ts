export const getPuzzleById = (id: number): ChessPuzzle | undefined => {
  return chessPuzzles.find(puzzle => puzzle.id === id);
};

export const getPuzzlesByDifficulty = (difficulty: string): ChessPuzzle[] => {
  return chessPuzzles.filter(puzzle => puzzle.difficulty === difficulty);
};

export const getRandomPuzzle = (difficulty?: string): ChessPuzzle => {
  const puzzles = difficulty 
    ? getPuzzlesByDifficulty(difficulty)
    : chessPuzzles;
  return puzzles[Math.floor(Math.random() * puzzles.length)];
};
